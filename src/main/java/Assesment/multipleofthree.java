package Assesment;

public class multipleofthree {
    public static void main(String[] args) {

        System.out.println("multiple of 3 from 1 to 20");

        // run for loop
        for (int i=1; i<=20; i++){
            if( i % 3==0){
                System.out.println(i +" ");

            }
        }

    }
}
